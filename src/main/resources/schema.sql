
/* Table login */
create table login (
    id integer generated by default as identity (start with 1, increment by 1) not null primary key,
    user_name varchar(32) not null, 
    password varchar(32) not null, 
    enabled boolean not null, 
    role varchar(16) not null
);

/* Table product */
create table products(
    id integer generated by default as identity (start with 1, increment by 1) not null primary key, 
    name varchar(50) not null, 
    description varchar(250),
    image varchar(150) 
);

/* Table answer_types */
create table answertypes(
    id integer generated by default as identity (start with 1, increment by 1) not null primary key,
    answertype varchar(10) not null
);

/* Table questionnaires_options */
create table answervalues(
    id integer generated by default as identity (start with 1, increment by 1) not null primary key,
    question varchar(128) not null,
    answervalue varchar(150) not null   
);

/* Table questionnaires */
create table questionnaires(
    id integer generated by default as identity (start with 1, increment by 1) not null primary key, 
    productid integer not null,
    sequencenumber integer not null,
    questionid integer not null,
    answertypeid integer not null,
    dependson integer,
    ifanswer varchar(128),
    constraint questionnaires_fk foreign key (productid) references products (id),
    constraint questionnaires_fk_answervalues foreign key (questionid) references answervalues (id),
    constraint questionnaires_fk_answers foreign key (answertypeid) references answertypes (id)
);


